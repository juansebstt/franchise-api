package com.franchiseapp.commons.constants;

/**
 * This class holds the API path constants for the Franchise application.
 * These constants define the versioning and base paths for various resources
 * in the API.
 */
public class ApiPathConstants {

    /**
     * The base route for version 1 of the API.
     */
    public static final String V1_ROUTE = "/v1";

    /**
     * The route for accessing franchise-related endpoints.
     */
    public static final String FRANCHISE = "/franchise";

    /**
     * The route for accessing branch-related endpoints.
     */
    public static final String BRANCH = "/branch";

    /**
     * The route for accessing product-related endpoints.
     */
    public static final String PRODUCT = "/product";

}
